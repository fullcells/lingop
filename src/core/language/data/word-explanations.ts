export const wordExplanationsLookup: WordExplanationsLookup = Object.fromEntries(
  Object.entries(wordExplanationsByLang).map(([lang, dict]) => [
    lang,
    Object.entries(dict).reduce(
      (acc, [key, explanation]) => {
        for (const variant of key.split("/")) {
          const normalized = normalizeWordExplanationKey(variant);
          (acc[normalized] ??= []).push(explanation);
        }
        return acc;
      },
      {} as Record<string, string[]>,
    ),
  ]),
);

export function getWordExplanationsForWord(lang: string, word: string): string[] {
  return wordExplanationsLookup[lang]?.[normalizeWordExplanationKey(word)] ?? [];
}

function normalizeWordExplanationKey(word: string): string {
  return word.trim().toUpperCase();
}
